package reportManagement;

import java.util.Date;

public class ProjectTask {

	private String projectName;
	private String employeeName;
	private Date date;
	private int hours;

	public String getProjectName() {
		return projectName;
	}

	public String getEmployeeName() {
		return employeeName;
	}

	public Date getDate() {
		return date;
	}

	public int getHours() {
		return hours;
	}

}
